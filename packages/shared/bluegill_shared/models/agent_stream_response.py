from pydantic import BaseModel
from typing import Literal

from bluegill_shared.models.message import Message


AgentEvent = Literal["generate", "tool_call", "tool_result", "error"]


class AgentStreamResponse(BaseModel):
    """
    A model that defines async stream chunks yielded by the agent.
    
    Attributes:
        type: The type of message this chunk contains. Possible values are:
            - generate: The response contains standard LLM output.

            - tool_call: The response contains a formatted tool call.

            - tool_result: The response contains the output of tool execution.

            - error: Denotes that an agent error occurred. The response contains details about the error.
            
            - end: Signifies that the stream ended successfully. 
            
        done: Signifies the final chunk in the stream.
            
        content: The content of the agent's response.

        tool: The name of the tool used if "event" is either "tool_call" or "tool_result".

        input: The input parameters for the given tool.

        total_duration: Time spent in nanoseconds calling tools and generating responses. Only populated when event == "done".

        token_count: The total number of tokens used by the model. Only populated when event == "done".
        
        context: A tuple containing the full context of the conversation with index 0 containing  messages passed to the agent
                  and index 2 containing messages generated by the agent during the current run_agent() call. 
                  Only populated when event == "done".
    """
    
    event: AgentEvent | None = None
    done: bool = False
    content: str | None = None
    tool: str | None = None
    input: dict | None = None
    total_duration: int | None = None
    token_count: int | None = None
    context: tuple[list[Message], list[Message]] | None = None
